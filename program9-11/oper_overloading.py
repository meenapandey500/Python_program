x,y=20,40
print("Sum : ",x+y)
print("sum of 2 nos : ",x.__add__(y))
print("Subtract : ")
print(x-y)
print(x.__sub__(y))

print("Multiply : ")
print(x*y)
print(x.__mul__(y))

print("True Division ")
print(x/y)
print(x.__truediv__(y))

print("Integer division ")
print(x//y)


print("Remainder ")
print(x%y)
print(x.__mod__(y))


